    private void SelectEventoStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_SelectEventoStateChanged
        
        String seleccion = SelectEvento.getValue().toString();

        if (seleccion.equals("Humedades en paredes")) {
            jComboBox1.setEnabled(true);   // habilita el campo
            jTextArea1.setEnabled(false); //Desactivar el area de etxto si seleccionamos Humedades en paredes
        }else if(seleccion.equals("Otros")){ //Si elegimos la opcion de otros que se habilite el texto 
            jTextArea1.setEnabled(true);
            jComboBox1.setEnabled(false);
        }else{//Por si elejimos cualquier otra opcion
            jComboBox1.setEnabled(false);
            jTextArea1.setEnabled(false);
        }
            
                   
        
        
        
    }//GEN-LAST:event_SelectEventoStateChanged

